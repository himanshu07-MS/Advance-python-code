
# Q.6: write a program in python to calculate student gread based on their marks/scores.

n = int(input("Enter obtained marks between 0-100: "))
if n>=90 :
    print("Gread A+")
elif n>=80:
    print("Gread A")
elif n>=70:
    print("Gread B")
elif n>=50:
    print("Gread c")
elif n>=34:
    print("Gread D")
else:
    print("fail.........")
